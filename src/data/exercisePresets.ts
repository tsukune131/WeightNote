function parse(raw: string): ExercisePreset[] {
  const out: ExercisePreset[] = [];
  let cat = '';
  for (const line of raw.split('\n')) {
    const t = line.trim();
    if (!t) continue;
    if (t.startsWith('#')) {
      cat = t.slice(1).trim();
      continue;
    }
    const [name, kana, mets] = t.split('\t');
    if (!name || !mets) continue;
    out.push({ name, kana: kana || name, mets: Number(mets), cat });
  }
  return out;
}

export const EXERCISE_PRESETS: ExercisePreset[] = parse(RAW);
