/**
 * 運動の強度(METs)テーブル(同梱データ)。
 *
 * 消費カロリーは種目だけでは決まらず、体重と時間で変わる。
 * そのため料理のように「1回あたりkcal」ではなく強度(METs)で持ち、
 * 体重と実施時間から推定する(計算は lib/calc.ts の metsToKcal)。
 *
 * 値は国立健康・栄養研究所「改訂版 身体活動のメッツ表」などで
 * 一般に使われている代表値。同じ種目でも強度で大きく変わるため、
 * UI上では必ず「目安」と示すこと。
 *
 * データ形式: `名前\tよみ\tMETs` の行。`#` 始まりの行はカテゴリ見出し。
 * よみはひらがな(検索用)。名前だけで引ける物は名前と同じで良い。
 */

export interface ExercisePreset {
  name: string;
  kana: string;
  mets: number;
  cat: string;
}

